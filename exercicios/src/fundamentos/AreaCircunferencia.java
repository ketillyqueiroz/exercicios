package fundamentos;

public class AreaCircunferencia {
	
	public static void main(String[] args) {
		double raio = 3.4;
		double PI = 3.14159;
		
		double area = PI * raio * raio;
		
		System.out.println(area);
		
		int velocidade = 30;
		int tempo = 5;
		
		int aceleracao = velocidade / tempo;
		
		System.out.println(aceleracao); 
		
	}
}
